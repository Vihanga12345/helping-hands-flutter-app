  /// Initialize the localization service
  Future<void> initialize() async {
    if (_isInitialized) return;

    try {
      final prefs = await SharedPreferences.getInstance();
      _currentLanguage = prefs.getString('selected_language') ?? englishCode;
      _isInitialized = true;
      print(
          'тЬЕ LocalizationService initialized with language: $_currentLanguage');
      notifyListeners();
    } catch (e) {
      print('Error initializing LocalizationService: $e');
      _isInitialized = true;
      notifyListeners();
    }
  }

  /// Get translated text
  String translate(String text) {
    if (_currentLanguage == englishCode) {
      return text;
    }

    // Check offline translations first
    if (_offlineTranslations.containsKey(_currentLanguage)) {
      final translations = _offlineTranslations[_currentLanguage]!;
      if (translations.containsKey(text)) {
        print(
            'ЁЯФД Translating "$text" to "${translations[text]}" ($_currentLanguage)');
        return translations[text]!;
      }
    }

    // Return original text if no translation found
    print('тЪая╕П No translation found for "$text" in $_currentLanguage');
    return text;
  }

  /// Change language
  Future<void> changeLanguage(String languageCode) async {
    if (_currentLanguage == languageCode) return;

    try {
      _currentLanguage = languageCode;
      final prefs = await SharedPreferences.getInstance();
      await prefs.setString('selected_language', languageCode);
      print('тЬЕ Language changed to: $languageCode');
      notifyListeners();
    } catch (e) {
      print('Error changing language: $e');
    }
  }

  /// Get supported languages
  List<String> getSupportedLanguages() {
    return [englishCode, sinhalaCode, tamilCode];
  }

  /// Get native language name
  String getNativeLanguageName(String languageCode) {
    switch (languageCode) {
      case englishCode:
        return 'English';
      case sinhalaCode:
        return 'р╖Гр╖Тр╢Вр╖Др╢╜';
      case tamilCode:
        return 'родрооро┐ро┤рпН';
      default:
        return 'English';
    }
  }
